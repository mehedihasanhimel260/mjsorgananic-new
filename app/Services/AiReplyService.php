<?php

namespace App\Services;

use App\Models\Faq;
use App\Models\Product;
use App\Models\User;

class AiReplyService
{
    public function generateReply(User $user, string $message): string
    {
        $directFaqAnswer = $this->findFaqAnswer($message);

        if ($directFaqAnswer) {
            return $directFaqAnswer;
        }

        $aiPrompt = $this->buildAiPrompt($user, $message);
        $aiResponse = active_ai_response($aiPrompt);
        $reply = $aiResponse['success']
            ? $aiResponse['message']
            : $this->fallbackMessage();

        $this->storePendingFaq($message, $reply);

        return $reply;
    }

    public function fallbackMessage(): string
    {
        return '??????, ?? ????? ?? ???????? ???? ???? ???? ????? ??? ??????? ??? ?????? WhatsApp ????? 01309003117 ? ??????? ?????';
    }

    private function normalizeMessageForKeywordMatch(string $message): string
    {
        $normalized = preg_replace('/[^\p{L}\p{N}\s]+/u', ' ', mb_strtolower($message));

        return trim(preg_replace('/\s+/', ' ', $normalized ?? ''));
    }

    private function findFaqAnswer(string $message): ?string
    {
        $normalizedMessage = $this->normalizeMessageForKeywordMatch($message);

        if ($normalizedMessage === '') {
            return null;
        }

        $faqs = Faq::query()
            ->select('question', 'answer', 'keyword', 'status')
            ->where('status', Faq::STATUS_ACTIVE)
            ->latest()
            ->get();

        foreach ($faqs as $faq) {
            $normalizedQuestion = $this->normalizeMessageForKeywordMatch($faq->question);

            if (
                $normalizedQuestion !== ''
                && (
                    str_contains($normalizedMessage, $normalizedQuestion)
                    || str_contains($normalizedQuestion, $normalizedMessage)
                )
            ) {
                return $faq->answer;
            }

            foreach ($faq->keyword_list as $keyword) {
                $normalizedKeyword = $this->normalizeMessageForKeywordMatch($keyword);

                if ($normalizedKeyword !== '' && str_contains($normalizedMessage, $normalizedKeyword)) {
                    return $faq->answer;
                }
            }
        }

        return null;
    }

    private function storePendingFaq(string $message, string $reply): void
    {
        $normalizedMessage = $this->normalizeMessageForKeywordMatch($message);

        if ($normalizedMessage === '') {
            return;
        }

        $existingFaq = Faq::query()
            ->select('id', 'question')
            ->get()
            ->first(function (Faq $faq) use ($normalizedMessage) {
                return $this->normalizeMessageForKeywordMatch($faq->question) === $normalizedMessage;
            });

        if ($existingFaq) {
            $existingFaq->update([
                'answer' => $reply,
                'status' => Faq::STATUS_PENDING,
            ]);

            return;
        }

        Faq::create([
            'question' => $message,
            'answer' => $reply,
            'status' => Faq::STATUS_PENDING,
            'keyword' => [],
        ]);
    }

    private function buildFaqContext(): string
    {
        $faqs = Faq::query()
            ->select('question', 'answer', 'keyword')
            ->where('status', Faq::STATUS_ACTIVE)
            ->latest()
            ->take(20)
            ->get();

        if ($faqs->isEmpty()) {
            return 'FAQ data not available.';
        }

        return $faqs->map(function (Faq $faq, int $index) {
            return ($index + 1).'. Question: '.$faq->question
                .' | Answer: '.$faq->answer
                .' | Keyword: '.($faq->keyword_list ? implode(', ', $faq->keyword_list) : 'N/A');
        })->implode("\n");
    }

    private function buildProductContext(): string
    {
        $products = Product::query()
            ->where('status', 'active')
            ->select('name', 'selling_price', 'short_description', 'long_description', 'keywords')
            ->latest()
            ->take(20)
            ->get();

        if ($products->isEmpty()) {
            return 'Product data not available.';
        }

        return $products->map(function (Product $product, int $index) {
            return ($index + 1).'. Product: '.$product->name
                .' | Price: '.$product->selling_price
                .' | Short Description: '.($product->short_description ?: 'N/A')
                .' | Details: '.($product->long_description ?: 'N/A')
                .' | Keywords: '.($product->keywords ?: 'N/A');
        })->implode("\n");
    }

